#displaying week days
day=int(input("enter the number ofday:"))
if day==1:
    print("it's monday")
elif day==2:
    print("it's tuesday")
elif day==3:
    print("it's wednesday")
elif day==4:
    print("it's thursday")
elif day==5:
    print("it's friday")
elif day==6:
    print("it's saturday")
else:
    print("it's sunday")